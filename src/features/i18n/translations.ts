function romanizeHangul(text: string) {
  return text.replace(/[\uAC00-\uD7A3]+/g, (word) =>
    word
      .split('')
      .map((character) => {
        const syllableIndex = character.charCodeAt(0) - 0xac00
        const initialIndex = Math.floor(syllableIndex / 588)
        const medialIndex = Math.floor((syllableIndex % 588) / 28)
        const finalIndex = syllableIndex % 28

        return `${initialConsonants[initialIndex]}${medialVowels[medialIndex]}${finalConsonants[finalIndex]}`
      })
      .join(''),
  )
}

