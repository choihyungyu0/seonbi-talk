function translateByTerms(text: string) {
  return sortedTermTranslations.reduce(
    (currentText, [korean, english]) => currentText.split(korean).join(english),
    text,
  )
}

function applyUnitTranslations(text: string) {
  return text
    .replace(/(\d+)\s*곳/g, '$1 places')
    .replace(/(\d+)\s*개/g, '$1 items')
    .replace(/(\d+)\s*명/g, '$1 visitors')
    .replace(/(\d+)\s*분/g, '$1 min')
    .replace(/(\d+)\s*시간/g, '$1 hr')
    .replace(/오전/g, 'AM')
    .replace(/오후/g, 'PM')
}

